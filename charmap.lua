end

function getName(offset)
	offset = offset + 8
	name = ""
	for offsetChar = 0, 9 do
		charByte = memory.readbyteunsigned(offset + offsetChar)
		if  charByte == 0xFF then
			return name
		else
			name = name .. byteToChar(charByte)
		end
	end
	return name
end